		        	<ul>
		        		<li>A crafting system has been made so that everyone can craft their own weapons more specific to their playstyle</li>
		        		<li>Two parts are required, a "base" (like a hilt) and a "main" part (like a blade), both the base and main parts have different amounts of slots as well as stats such as damage, crit, str requirements</li>
		        		<li>The main parts can be modified with parts of the type "extra" to improve the stats</li>
		        		<li>Parts are aquired through adventures (each adventure has a 10% chance to award you with a part). They will also be gained by fighting in the arena versus other players, but that is not yet implemented</li>
						<li>There has been no tests or balancing done with the crafting, so expect bugs and quite powerful weapons ;)</li>
						<li>Only melee weapons can be crafted</li>
