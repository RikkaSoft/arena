	<h2>Guide for new players</h2>
	
	<h3>Creating your gladiator</h3>
	<p><strong>Choose your race</strong> <br>All races have different starting stats and you should pick whichever fits your desired gladiator.
		This information is already available in the character creation page but is still worth mentioning.
		<br>
		Choose your primary stats and skills based on what kind of gladiator you wish to play as.
		<br> I'll write this all the way up here because it's the most asked question: How do I regenerate my HP?
		<br> Your regenerate 40% of you HP every three minutes, so just check back in a while =)
	</p>
	<br>
	<h3>Primary stats:</h3>
	<p>
		<strong>Strength:</strong> Increases the damage you do in combat, obvious choice for anyone trying to focus on finishing their opponent in as few rounds as possible.<br> Weapons also have a strength requirement
		which vary. For example: Daggers have a very low strenght requirement where as BattleAxes requires a really strong gladiator.
	</p>
	<p>
		<strong>Dexterity:</strong> Increases your ranged damage (will be implemented in a later patch). This also increases your chance to dodge as well as your critical hit chance.
		Keep in mind that the skill "Finesse" increases your crit chance even more. (This is cumulative of course so combining these two is one way to go.)
	</p>
	<p>
		<strong>Vitality:</strong> Increases your health. You get one health for each point spent in vitality
	</p>
	<p>
		<strong>Intellect:</strong> Increases the amount of skill points you get to spend when you create your character and whenever you level up. 
		<br>Surely a good choice for anyone wanting to focus on their skills.
	</p>
	
	<br>
	<h3>Skills:</h3>
	<p>
		I'm not going to go through every skill separately since they all have tooltips in the creation page but I'll explain the basics.
		<br><br>
		<strong>Weapon skills</strong> increase your chance to hit. 
		<br>All weapons have a recommended skill amount. You do <strong><u>not</u></strong> need to have the recommended amount to use it but your chance to hit is decreased if your skills are not high enough.
		<br><br>
		<strong>Shield:</strong> Increases your block chance. 
		<br>Shields, just like the weapons skills doesn't actually require the skill, it's just that they will be horrible without any skill.
		<br>A block removes all damage of your opponents attack. In a future patch the shields will be given a durability. It will perhaps only block a certain amount of times or damage until it breaks.
		<br><br>
		<strong>Parry:</strong> Gives you the ability to parry, without this skill you cannot parry at all. Parry chance is increased by your amount of skill points.
		<br>A successful parry will complete negate your opponents attack and still let you attack.
		<br><br>
		<strong>Foul play:</strong> This is an amazing skill, one of the most powerful skills I would say. It gives you a chance to dodge an attack, throwing sand in your opponents eyes, taking away their ability to attack in the next round. Thus avoiding taking damage twice.
		<br>The chance of this skill actually working is very low, but with enough points in it it may be effective!
		<br><br>
		<strong>Armour skills:</strong> These are important if you plan to use armour. All armour requires this skill. Cloth armour requires at least 20 points in the Light armour skill and Bronze armour requires at least 30 points in the Heavy armour skill. 
		<br>The skill does not increase the amount of damage a piece of armour can absorb.
		<br><br>
		<strong>Finesse:</strong> Increases your chance to crit. 
		<br>Different weapons have different crit bonuses such as daggers having a lower base damage but a greater crit bonus.
		<br>								
	It is a trial and error kind of game, find your favorite playstyle and stick to it is my tip.
	</p>
	<br>
	<h3>Leveling</h3>
	
	Fighting beasts in Training is probably the fastest way to level up, at least at this time where we don't have that many players fighting in the arena.
	<p>
	Pick a beast to fight against and choose at what percentage of health you wish to surrender, just in case the beast gets the better of you.
	<br>
	Keep in mind, beasts cannot get critical hits! So the damage displayed in their creature sheet is their lowest and maximum damage.
	<br>
	Beasts have levels, with the giant rat being the weakest at level 1 and the large bear highest at level 8, keep in mind there will be alot more beasts added in a near future.
	<br>
	Your level changes the rewards for defeating a beast, thus fighting a beast that is a higher level than you will yield greater rewards and fighting a beast that is a lower level than you will
	result in a lower or, in some cases, no reward at all.
	</p>
	<br>
	
	<h3>Fighting in the Arena</h3>
	
	<p>When you fight in the Arena you fight other gladiators. Definitely the most fun part and the core of the game, this will be your greatest source of gold and experience.
	<br>
	Just as when fighting beasts you choose the percentage of health you wish to surrender at but there's another option! You can choose to battle someone at a higher level than yourself.
	<br>
	Defeating someone who is a higher level than you will yield greater rewards!
	<br>
	After pressing "Fight!" you are put in a queue to the Arena. Once a matching gladiator has been found your match will begin and you will find a battle report if you enter the arena again. You will not leave the queue if you do not leave it yourself! After you log out you will still be in queue for a match.
	<br>
	</p>
	
	<br>
	
	<h3>Items</h3>
	<p>
	Wearing armour decreases your hit chance. The weight of the armour determines the amount of lost hit chance. For example, having 4 pieces of cloth armor, 
	all having a weight of 1 equals 4% lost hit chance. 
	<br>This makes heavy armour incredibly detrimental to your hit chance amounting up to a 25% loss! 
	<br>The only way to counter this would be to spend alot of skill points in the corresponding weapon skill.
	
	Also, keep in mind. Head armour does not protect your legs. So the armour does to cumulate as one would possibly think. Being hit in the leg with cloth armor equipped would absorb 1 damage, even if you would have cloth armor equipped in all four armour slots.
	
	<br><br>
	Each character starts with 125 gold in their inventory, this is enough to buy whatever else you plan on using other than the items you begin with.
	<br>
	Buy your desired weapons and armour and you are ready to go!
	<br><br>
	Don't forget to equip your items before fighting as they are not automatically equipped after you buy them.
	</p>
